// wav2vec2-XLSR-53 (Korean) CTC worker — ALL inference off the main thread.
//
// DISTINCT model from every other ASR demo in this showcase. Like the built French/Spanish/Italian/
// Portuguese/Russian XLSR demos it is wav2vec2-large-XLSR-53 (cross-lingual, pre-trained on 53 languages,
// 56k hours) — but this checkpoint is fine-tuned on KOREAN (jonatasgrosman/wav2vec2-large-xlsr-53-korean).
// The decisive difference: its CTC head is NOT a Latin alphabet. It emits **whole Hangul SYLLABLE blocks**
// from a 1205-symbol vocabulary (가 각 간 … 힘), so the per-frame argmax is a stream of complete Korean
// syllables, not letters. Feeding it a Latin language yields Korean-phonetic gibberish; it is
// language-specialised for Korean. That syllable-level CTC over Hangul is the point.
//
// Model: FinDIT-Studio/wav2vec2-large-xlsr-53-korean-onnx (ONNX export of jonatasgrosman's Korean XLSR-53).
// This repo ships ONE fp32 ONNX at the REPO ROOT (no onnx/ subfolder) plus a tokenizer.json, but NO
// config.json and NO preprocessor_config.json. So, exactly like the built Spanish XLSR demo:
//   • we load the ONNX with subfolder:"" + dtype:"fp32" (no q8/int8 export exists — fp32 is the honest one;
//     the single self-contained model.onnx is ~1.2 GB);
//   • the missing config.json means AutoModel can't infer the architecture, so we load the wav2vec2-large
//     XLSR-53 architecture config from a REACHABLE sibling repo (jonatasgrosman's Spanish XLSR-53 — the
//     Korean base repo is access-gated) and override vocab_size→1205 + pad_token_id→1204 so it matches the
//     Korean CTC head. This is architecture config only (identical across every XLSR-53 fine-tune); no
//     Spanish weights are used — the weights are 100% the Korean model.onnx;
//   • the missing preprocessor_config.json means we do the wav2vec2 feature extraction ourselves —
//     zero-mean/unit-variance normalisation of the raw 16 kHz waveform (do_normalize / feat_extract_norm
//     "layer" for XLSR-large);
//   • we do the greedy CTC decode ourselves from the model's 1205-symbol Hangul vocab (embedded below,
//     inverted from the repo's tokenizer.json), which also surfaces the raw per-frame syllable strip + the
//     CTC collapse + real per-word forced-alignment timings.
// Verified runnable BEFORE building (transformers.js 3.7.5, WASM, headless Chrome): logits dims
// [1, T, 1205]; CC0 clip "가수" → "가수", a 12 s CC0 Korean speech clip → coherent running Hangul, and an
